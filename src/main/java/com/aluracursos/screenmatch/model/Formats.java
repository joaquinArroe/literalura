package com.aluracursos.screenmatch.model;

class Formats {
    private String mimeType;
    private String url;

    // Getters and setters
    public String getMimeType() {
        return mimeType;
    }

    public void setMimeType(String mimeType) {
        this.mimeType = mimeType;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}